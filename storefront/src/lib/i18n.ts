export function normalizeStoreLocale(locale: string | null | undefined): StoreLocale {
  return locale?.toLowerCase().startsWith("fr") ? "fr" : "ar";
}

export function storeLocale(store: Store): StoreLocale {
  return normalizeStoreLocale(store.locale);
}

export function localeDirection(locale: StoreLocale | string | null | undefined): StoreDirection {
  return normalizeStoreLocale(locale) === "ar" ? "rtl" : "ltr";
}

export function localeTag(locale: StoreLocale | string | null | undefined): "ar-DZ" | "fr-DZ" {
  return normalizeStoreLocale(locale) === "ar" ? "ar-DZ" : "fr-DZ";
}

export function getStorefrontCopy(locale: StoreLocale | string | null | undefined) {
  return storefrontCopy[normalizeStoreLocale(locale)];
}
